<h2 ><font color=#F7DC6F>Objetivos</font></h2>
<p align="justify"><font color=#F1C40F>El objetivo principal de este proyecto es la creación de una aplicación web denominada Odonto, para poder acceder a ella desde cualquier red, no solo desde la LAN de la facultad de odontología. Esta aplicación estará enfocada a un fácil uso, incorporando valores por defecto en los campos que sea posible, consiguiendo una mayor rapidez en la incorporación de datos a las historias clínicas de los pacientes, además para facilidad de profesores y alumnos se creará un manual enfocado a ellos en el que se especifique el uso de la aplicación.
        Debido a lo amplia que es la aplicación actual, en este proyecto se va a replicar y mejorar únicamente el módulo de recepción de un paciente en su historia clínica, el cual consta de:<br>
        -Datos de usuarios: profesores y alumnos.<br>
        -Datos de los pacientes.<br>
        -Exámenes clínicos que contienen datos del examen extraoral, la fluorosis, el examen de la mucosa oral, el ICP, el tipo de angle, la dentición, la discrepancia y la opacidad.
    </font></p>

